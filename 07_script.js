// ARITHMETIC OPERATION

let a=100;
let b=20;
let c;

//Addition
c=(a+b);
console.log(c);
console.log(typeof c);

//Subtraction
c=(a-b);
console.log(c);

//Multiplication
c=(a*b);
console.log(c);

//Division
c=(a/b);
console.log(c);

//modulus
c=(a%b);
console.log(c);

//power  (2016)
c=(15**2);
console.log(c);

//Increment & Decrement
c=++a;
console.log(c);
c=--b;
console.log(c);




